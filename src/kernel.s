%include    "define.s"
%include    "macro.s"

    ORG     KERNEL_LOAD                         ; カーネルのロードアドレス

[BITS 32]
;************************************************************************
;   エントリポイント
;************************************************************************
kernel:
        ;--------------------------------
        ; 処理の終了
        ;--------------------------------
        jmp     $                               ; while (1) ;   // 無限ループ

        ;--------------------------------
        ; データ
        ;--------------------------------
.s0:    db  "Kernel...", 0x0A, 0x0D, 0

;************************************************************************
;   パティング
;************************************************************************
        times KERNEL_SIZE - ($ - $$) db 0
